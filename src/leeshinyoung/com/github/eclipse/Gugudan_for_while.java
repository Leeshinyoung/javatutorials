package leeshinyoung.com.github.eclipse;

public class Gugudan_for_while {

	public static void main(String[] args) {
		//while을 이용한 6단 
		int i = 1;
		while (i < 10) {
			System.out.println(6 * i);
			i += 1;
		}
		//for를 이용한 7단 
		for (int j = 1; j<10; j++) {
			System.out.println(7 * j);	
		}
	}
}

